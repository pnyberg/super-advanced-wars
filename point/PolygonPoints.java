package point;

public class PolygonPoints {
	private int[] xPoints;
	private int[] yPoints;
	
	public PolygonPoints(int[] xPoints, int[] yPoints) {
		this.xPoints = xPoints;
		this.yPoints = yPoints;
	}
	
	public int[] getXPoints() {
		return xPoints;
	}
	
	public int[] getYPoints() {
		return yPoints;
	}
}
